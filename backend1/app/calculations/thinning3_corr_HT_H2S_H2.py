    def get_corrosion_rate(self, material, temp_f, h2s_mole_pct, hydrocarbon_type="Naphtha"):
        # Normalización de entrada de material
        # mat = str(material).upper().replace(" ", "")
        mat = str(material)

        if any(x in mat for x in ["Carbon Steel", "1Cr-1/5Mo", "1Cr-1/2Mo", "1 1/4Cr-1/2Mo", "2 1/4Cr-1Mo", "3Cr-1Mo"]):
            key = "Low Chrome (CS to 3Cr)"
        elif "5Cr-1/2Mo" in mat:
            key = "5CR"
        elif "7Cr" in mat: 
            key = "7CR"
        elif "9Cr-1Mo" in mat:
            key = "9CR"
        elif "12Cr" in mat:
            key = "12Cr Steel"
            hydrocarbon_type = "Standard"
        elif any(x in mat for x in ["Type 304 Stainless Steel", "Type 304L Stainless Steel", 
                                    "Type 316 Stainless Steel", "Type 316L Stainless Steel", 
                                    "Type 321 Stainless Steel", "Type 347 Stainless Steel"]):
            key = "300 Series SS"
            hydrocarbon_type = "Standard"
        else:
            # En lugar de un string, podrías lanzar una excepción o retornar 0.0
            return 0.0

        data = np.array(self.data_tables[key][hydrocarbon_type])
        
        # El interpolador requiere una tupla de los puntos de la grilla
        interp_func = RegularGridInterpolator((self.h2s_levels, self.temps), data, bounds_error=False, fill_value=None)
        
        # El punto debe estar envuelto en una lista adicional para evitar ambigüedad en versiones recientes de SciPy
        point = np.array([[h2s_mole_pct, temp_f]])
        rate = interp_func(point)
        
        # CORRECCIÓN: Accedemos al primer elemento [0] antes de convertir a float
        return round(float(rate[0]), 2)
    

