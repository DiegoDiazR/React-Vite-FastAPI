    def get_corrosion_rate(self, material, conc, temp, vel, oxidant=False):
        """Metodología API 581 con Fix de TypeError para Scalars"""
        if material == "Alloy B-2" and oxidant:
            return "BLOQUEADO: Consultar especialista (Alloy B-2 falla con oxidantes)."

        if material not in self.tables:
            return f"Error: Material '{material}' no definido."

        c_grid_raw, t_grid, v_grid, r_grid_raw = self.tables[material]

        # RegularGridInterpolator requiere que todos los ejes sean ASCENDENTES
        is_c_desc = c_grid_raw[0] > c_grid_raw[-1]
        c_grid = c_grid_raw[::-1] if is_c_desc else c_grid_raw
        r_grid = r_grid_raw[::-1] if is_c_desc else r_grid_raw

        # Clampeado de valores para evitar errores de dominio
        c_in = np.clip(conc, c_grid.min(), c_grid.max())
        t_in = np.clip(temp, t_grid.min(), t_grid.max())
        v_in = np.clip(vel, v_grid.min(), v_grid.max())

        try:
            interp = RegularGridInterpolator((c_grid, t_grid, v_grid), r_grid)
            # FIX: .item() extrae el valor del array de numpy a un escalar de Python
            rate = interp([c_in, t_in, v_in])[0].item()
            return round(float(rate), 2)
        except Exception as e:
            return f"Error de cálculo: {str(e)}"

