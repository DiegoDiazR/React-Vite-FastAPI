    def calculate(self, material, temperature, sulfur, tan, velocity):
        """
        Determina la tasa de corrosión según la metodología de Figura 2.B.3.1.
        """
        # Validación mínima de temperatura
        if temperature < 400: #
            return {"status": "Inactivo", "rate": 0.0, "reason": "T < 400°F"}

        if material not in self.db:
            return {"status": "Error", "reason": f"Material '{material}' no configurado."}

        # Interpolación base
        point = np.array([sulfur, tan, np.clip(temperature, 450, 800)])
        try:
            base_rate = float(self.db[material](point)[0])
        except ValueError:
            return {"status": "Error", "reason": "Valores de S o TAN fuera de rango."}

        # Factor de Velocidad (> 100 ft/s -> Rate * 5)
        mult = 5.0 if velocity > 100 else 1.0
        final_rate = base_rate * mult

        return {
            "Material": material,
            "Tasa Base (mpy)": round(base_rate, 2),
            "Factor Velocidad": f"x{mult}",
            "Tasa Final Estimada (mpy)": round(final_rate, 2)
        }

